    id_perg INTEGER NOT NULL,
    id_alter INTEGER NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    correta BOOLEAN NOT NULL DEFAULT false,
    descricao VARCHAR(255) NOT NULL,
